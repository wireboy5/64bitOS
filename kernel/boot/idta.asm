[extern isr_handler]

isr_common_handler:
    pushaq
    
    mov ax, ds
    push rax

    mov ax, 0x8
    mov ds, ax
    mov es, ax
    mov fs, ax
    mov gs, ax

    call isr_handler

    pop rax
    mov ds, ax
    mov es, ax
    mov fs, ax
    mov gs, ax

    popaq

    add rsp, 8

    sti
    iret

[global flush_idt]

flush_idt:
    mov rax, [esp + 8]
    lidt [rax]
    ret